Monstro:: Monstro(Elemento elemento): elemento(elemento){
    if(elemento == AR){
        fraqueza = GRAMA;
        descricao = "\n============================================================================================================\nA sala esta toda revirada! Parece ate que passou um furacao! Parece que esse monstro e um elementar do Ar!\nDeseja ataca-lo com qual elemento \n============================================================================================================\n";
    }
    else if(elemento == GRAMA){
        fraqueza = FOGO;
        descricao = "\n============================================================================================================\nA sala esta toda suja de terra e lama! Parece que esse monstro e um elementar da Grama! \nDeseja ataca-lo com qual elemento?\n============================================================================================================\n";
    }
    else if(elemento == FOGO){
        fraqueza = AGUA;
        descricao = "\n============================================================================================================\nA sala esta quente, um calor de 40 graus! Parece que esse monstro e um elementar do Fogo! \nDeseja ataca-lo com qual elemento?\n============================================================================================================\n";
    }
    else{
        fraqueza = AR;
        descricao = "\n============================================================================================================\nA sala esta toda molhada, pegue seu pe de pato! Parece que esse monstro e um elementar da Agua! \nDeseja ataca-lo com qual elemento?\n============================================================================================================\n";
    }
}

